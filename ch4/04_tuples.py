# Tuples -- they are immutable(cannot be changed) data type in python
#==================

# tuples are created with () brackets
numbers = (45, 15, 23, 78)
print(numbers)

# Accessing items in tuples is same as strings and lists
print(numbers[1])

# but number[1] = 77 ---> this is not allowed in tuples


