a = 5
b = 6

# Arthematic Operator like: ( +, -, /, * )
print("Addition of 5 and 6 =", a+b)

# Assignment Operator like: ( =, +=, -=)
b += 2
print(b)

# Comparison Operator like: ( ==, != )
print(4 == 4)

# Logical Operator like: (and, or, not)
bool1 = True
bool2 = False

print("Value of bool1 and bool2 is =", (bool1 and bool2))
print("Value of bool1 or bool2 is =", (bool1 or bool2))
print("Value of not bool2 is =", (not bool2))