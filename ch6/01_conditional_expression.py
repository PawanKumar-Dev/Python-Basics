# When we execute our code based on meeting certain conditions. This is called for conitional expression

# if else and elif in Python (Called if-else ladder)
a = 12
if(a > 40):
  print("A is greater than 40")
elif(a > 10):
  print("A is greater than 10")
else:
  print("A is 12")

# else part is optional. Hence your loop works fine without one