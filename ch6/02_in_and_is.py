# "is" check the value to be true or false(Works pretty similar to == operator)
a = 4
if(a is 4):
    print("Yes")
else:
    print("No")


# "in" checks whether a value is present inside a list or not
# "in" is also used in loops for iteration
li = [45, 16, 23, 78]
print(12 in li)
print(16 in li)